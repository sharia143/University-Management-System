<li>Minimum GPA of 2.5 in O-Levels in five subjects and A-Levels in two subjects according to the scale (A=5, B=4, C=3, D=2 &amp; E=1). Only one E is acceptable.</li>
</ul><p><strong>Admission Test</strong></p>
