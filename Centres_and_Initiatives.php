          <h4 style="text-align: left;">Centre for Climate Change and Environmental Research (C3ER)</h4>
<p style="text-align: left;">Since its inception, Brac University has conducted a series of cross-sectoral research on climate change and disaster management in direct collaboration with BRAC. To coordinate and manage these different activities, the Syndicate and the Board of Trustees of BRAC University have accorded for establishment of a research center titled “ Centre for Climate Change and Environmental Research (C3ER)".&nbsp;The Centre establishes a synergy between BracU and BRAC in the field of climate change and other environmental issues.</p>
<h4>Control and Applications Research Centre</h4>
<p>The Control &amp; Applications Research Centre (CARC) has expertise and activities in an area of systems and control engineering. CARC has a strong practical focus to the work, and many of the projects involve close collaboration with industrial organizations. We emphasis upon the application requirements and exercise established theoretical techniques to provide solutions through technological demonstration.</p>
<h4 style="text-align: left;">Centre for Entrepreneurship Development</h4>
<p style="text-align: left;"><em><strong>Centre for Entrepreneurship Development (CED)</strong></em> started its journey in April 2011 with the view to encourage Bangladeshi entrepreneurs and engender entrepreneurial knowledge and skill so that they can develop and grow their own businesses.</p>
<h4>Centre for Peace and Justice</h4>
<p>The Centre for Peace and Justice was recently established as multi-disciplinary post-graduate institute within Brac University. The mission of the Centre is ‘to promote global peace and social justice through the means of education and training, research and advocacy’ and with the vision of ‘a just, peaceful and inclusive society’</p>
<h4 style="text-align: left;">&nbsp;</h4>
<h4 style="text-align: left;">Professional Development Centre</h4>
<p style="text-align: left;">The Professional Development Centre (PDC) <strong>&nbsp;</strong>is an innovation of Brac University dedicated to promoting excellence in education through shared understanding and best practice. The centre was previously known as the Teaching &amp; Learning Centre, opened in 2006. The <strong>PDC</strong> strives to build a creative environment of pedagogical development and research that fosters innovation across the University in collaboration with faculty and administrative staff. The focus of the centre is to keep the campus community updated with pedagogical developments, coordinate academic resources, support professional development and contribute to successful student learning. From group events to personalized attention, the <strong>PDC</strong> supports measurable effectiveness in faculty development, course instruction and assessment, scholarly research, and innovation in education technologies. Our expanding activities and services include orientations, workshops and seminars, individual consultations, pedagogical research, teaching courses and programme design.</p>
<h4>Centre for Emotional Intelligence and Innovation</h4>
<p>CEII&nbsp;<em>(Centre for Emotional Intelligence and Innovation)</em> &nbsp;of Brac University is working to embed emotional intelligence in the education system through a comprehensive set of programs, research and innovation. We collaborate with key stakeholders among private, public, civil society and educational institutions to ensure that the institution develops next generation of leaders with 21st century skills. Adapting an innovation-based approach - we channel action-based ideas and solutions led by BracU students, faculty members and staffs in response to pressing socioeconomic needs.</p>
<h4>Centre for Inclusive Architecture and Urbanism (Ci+AU)</h4>
<p>Centre for Inclusive Architecture and Urbanism (Ci+AU) at Brac University is multidisciplinary research institute and offers research-based consultancy on urban issues – the exploration of conditions, processes, and policies that affect the livability, resilience, and sustainability of the built environment. The Centre's mission is to create a dynamic culture of architecture and urbanism that is inclusive, empathetic, environmentally conscious, and humanistic.</p>
<h4 style="text-align: left;">&nbsp;</h4>
<h4 style="text-align: left;">Certificate Programme on Environmental Management Governance (EMG)</h4>
<p style="text-align: left;">The Brac Institute of Governance and Development (BIGD), Brac University is offers <strong><em>Certificate Programme on Environmental Management and Governance</em></strong> jointly with the Centre for Natural Resource Studies of the University of Manitoba, Canada. The main purpose of the certificate programme is to sensitise and enhance the capacity of participants on key environmental governance issues related to natural resources and environment management. The certificate course is an intense programme which would provide opportunities to the participants to be trained with key concepts of environmental issues and concerns, management system, environmental governance, implementation methods and procedures.</p>
